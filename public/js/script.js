// Umumiy til o'zgartirish funksiyasi
function updateLanguage(lang) {
    document.querySelectorAll('[data-lang]').forEach(el => {
        const key = el.getAttribute('data-lang');
        const text = translations[lang][key];
        if (text !== undefined) {
            if (Array.isArray(text)) {
                el.innerHTML = text.map(item => `<li class="list-group-item">${item}</li>`).join('');
            } else {
                el.innerHTML = text;
            }
        }
    });

    document.querySelectorAll('[data-lang-placeholder]').forEach(el => {
        const key = el.getAttribute('data-lang-placeholder');
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Sahifa sarlavhasi
    if (translations[lang].homePageTitle) {
        document.title = translations[lang].homePageTitle;
    } else if (translations[lang].aboutPageTitle) {
        document.title = translations[lang].aboutPageTitle;
    } else if (translations[lang].legalPageTitle) {
        document.title = translations[lang].legalPageTitle;
    } else if (translations[lang].trustPageTitle) {
        document.title = translations[lang].trustPageTitle;
    } else if (translations[lang].faqPageTitle) {
        document.title = translations[lang].faqPageTitle;
    }

    document.documentElement.lang = lang;
}

// Sahifa yuklanganda
document.addEventListener('DOMContentLoaded', () => {
    const langSelect = document.getElementById('languageSelect');
    if (langSelect) {
        const savedLang = localStorage.getItem('selectedLanguage') || 'uz';
        langSelect.value = savedLang;
        updateLanguage(savedLang);

        langSelect.addEventListener('change', (e) => {
            const newLang = e.target.value;
            localStorage.setItem('selectedLanguage', newLang);
            updateLanguage(newLang);
        });
    } else {
        updateLanguage('uz');
    }

    // Form submit demo
    document.querySelectorAll('form').forEach(form => {
        form.addEventListener('submit', (e) => {
            e.preventDefault();
            alert('Xabar yuborildi! (demo)');
        });
    });
});