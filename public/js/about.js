// Tilni o'zgartirish funksiyasi (faqat about sahifasi uchun)
function updateAboutLanguage(lang) {
    document.querySelectorAll('[data-lang]').forEach(el => {
        const key = el.getAttribute('data-lang');
        const text = aboutTranslations[lang][key];
        if (text !== undefined) {
            el.innerHTML = text;
        }
    });

    // Sahifa sarlavhasi
    if (aboutTranslations[lang].aboutPageTitle) {
        document.title = aboutTranslations[lang].aboutPageTitle;
    }

    document.documentElement.lang = lang;
}

// Sahifa yuklanganda
document.addEventListener('DOMContentLoaded', () => {
    const langSelect = document.getElementById('languageSelect');
    if (langSelect) {
        const savedLang = localStorage.getItem('selectedLanguage') || 'uz';
        langSelect.value = savedLang;
        updateAboutLanguage(savedLang);

        langSelect.addEventListener('change', (e) => {
            const newLang = e.target.value;
            localStorage.setItem('selectedLanguage', newLang);
            updateAboutLanguage(newLang);
        });
    } else {
        updateAboutLanguage('uz');
    }
});