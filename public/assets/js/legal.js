function updateLegalLanguage(lang) {
    document.querySelectorAll('[data-lang]').forEach(el => {
        const key = el.getAttribute('data-lang');
        const text = legalTranslations[lang]?.[key] || legalTranslations.uz[key];
        if (text) {
            el.innerHTML = text;
        }
    });

    document.title = legalTranslations[lang]?.pageTitle || legalTranslations.uz.pageTitle;
}

document.addEventListener('DOMContentLoaded', () => {
    const langSelect = document.getElementById('lang');
    const mobileLang = document.getElementById('mobileLang');

    const savedLang = localStorage.getItem('selectedLanguage') || 'uz';

    if (langSelect) langSelect.value = savedLang;
    if (mobileLang) mobileLang.value = savedLang;

    updateLegalLanguage(savedLang);

    [langSelect, mobileLang].forEach(select => {
        if (select) {
            select.addEventListener('change', (e) => {
                const newLang = e.target.value;
                localStorage.setItem('selectedLanguage', newLang);
                if (langSelect) langSelect.value = newLang;
                if (mobileLang) mobileLang.value = newLang;
                updateLegalLanguage(newLang);
            });
        }
    });
});